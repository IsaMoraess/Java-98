public class BoasVindas{
    public void mensagem(){
        System.out.println("Bem vindos as curso de java do Salvador");
    }
}
/*  BoasVindas é a CLASSE
    mensagem é o METODO-> PUBLIC = acessesado por poutras classes, VOID = indica que esse método não retorna nenhum valor,MÉTODO = mensagem não recebe nenhum parâmetro.

    Classe: A classe BoasVindas serve como um contêiner para o método mensagem.
    Método: O método mensagem é responsável por imprimir a mensagem de boas-vindas.*/